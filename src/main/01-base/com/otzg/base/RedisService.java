    /**
     * redis添加数据存在即不添加
     *
     * @param key
     * @param value
     * @return
     */
    public boolean setIfAbsent(String key, Object value, long time) {
        try {
            if (redisTemplate.opsForValue().setIfAbsent(key, value)) {
                expire(key, time);
                return true;
            } else {
                return false;
            }
        } catch (Exception e) {
            e.printStackTrace();
            return false;
        }
    }

    /**
     * 枷锁
     * @param key
     * @param timestamp 失效时间秒
     * @return
     */
    public boolean lock(String key,long timestamp){
        while (((System.currentTimeMillis() - timestamp)/1000) < 15){
            if(setIfAbsent(key,System.currentTimeMillis(),15)){
                return true;
            }
        }
        return false;
    }

